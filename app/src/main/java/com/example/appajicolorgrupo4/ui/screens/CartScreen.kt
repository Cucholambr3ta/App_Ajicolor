@OptIn(ExperimentalMaterial3Api::class)
@Composable
fun CartScreen(
    navController: NavController,
    carritoViewModel: CarritoViewModel = viewModel()
) {
    val productos by carritoViewModel.productos.collectAsState()
    val subtotal by carritoViewModel.subtotal.collectAsState()
    val iva by carritoViewModel.iva.collectAsState()
    val costoEnvio by carritoViewModel.costoEnvio.collectAsState()
    val total by carritoViewModel.total.collectAsState()
    val calificaEnvioGratis by carritoViewModel.calificaEnvioGratis.collectAsState()

    val drawerState = rememberDrawerState(DrawerValue.Closed)
    val scope = rememberCoroutineScope()
    val currentRoute = navController.currentBackStackEntry?.destination?.route ?: ""

    val formatoMoneda = remember {
        NumberFormat.getCurrencyInstance(Locale.forLanguageTag("es-CL")).apply {
            maximumFractionDigits = 0
        }
    }

    AppBackground {
        AppNavigationDrawer(
            navController = navController,
            drawerState = drawerState,
            currentRoute = currentRoute
        ) {
            Scaffold(
                topBar = {
                    TopBarWithCart(
                        title = "Carrito de Compras",
                        navController = navController,
                        drawerState = drawerState,
                        scope = scope
                    )
                },
                bottomBar = {
                    BottomNavigationBar(
                        navController = navController,
                        currentRoute = currentRoute
                    )
                },
                containerColor = androidx.compose.ui.graphics.Color.Transparent
            ) { paddingValues ->
            Column(
                modifier = Modifier
                    .fillMaxSize()
                    .padding(paddingValues)
                    .padding(16.dp)
            ) {
                if (productos.isEmpty()) {
                    // Carrito vacÃ­o
                    Column(
                        modifier = Modifier
                            .fillMaxSize()
                            .weight(1f),
                        horizontalAlignment = Alignment.CenterHorizontally,
                        verticalArrangement = Arrangement.Center
                    ) {
                        // Imagen de carrito vacÃ­o
                        Image(
                            painter = painterResource(id = R.drawable.carrovacio),
                            contentDescription = "Carrito vacÃ­o",
                            modifier = Modifier
                                .size(200.dp)
                                .padding(bottom = 24.dp),
                            contentScale = ContentScale.Fit
                        )

                        Text(
                            text = "AÃºn no tienes productos en tu carrito",
                            style = MaterialTheme.typography.titleLarge,
                            textAlign = TextAlign.Center,
                            modifier = Modifier.padding(horizontal = 32.dp)
                        )

                        Spacer(modifier = Modifier.height(32.dp))

                        Button(
                            onClick = { navController.navigate(Screen.Catalogo.route) },
                            modifier = Modifier
                                .fillMaxWidth()
                                .padding(horizontal = 32.dp)
                                .height(56.dp)
                        ) {
                            Text(
                                text = "Ver CatÃ¡logo de Productos",
                                style = MaterialTheme.typography.titleMedium
                            )
                        }
                    }
                } else {
                    // Anuncio de envÃ­o gratis
                    Card(
                        modifier = Modifier.fillMaxWidth(),
                        colors = CardDefaults.cardColors(
                            containerColor = if (calificaEnvioGratis)
                                MaterialTheme.colorScheme.primaryContainer.copy(alpha = 0.7f)
                            else
                                MaterialTheme.colorScheme.secondaryContainer.copy(alpha = 0.6f)
                        )
                    ) {
                        Row(
                            modifier = Modifier
                                .fillMaxWidth()
                                .padding(12.dp),
                            verticalAlignment = Alignment.CenterVertically,
                            horizontalArrangement = Arrangement.spacedBy(12.dp)
                        ) {
                            Column(modifier = Modifier.weight(1f)) {
                                if (calificaEnvioGratis) {
                                    Text(
                                        text = "Â¡Felicidades! EnvÃ­o Gratis ðŸŽ‰",
                                        style = MaterialTheme.typography.titleMedium,
                                        fontWeight = FontWeight.Bold,
                                        color = MaterialTheme.colorScheme.primary
                                    )
                                    Text(
                                        text = "Tu compra supera los ${formatoMoneda.format(20000)}",
                                        style = MaterialTheme.typography.bodySmall
                                    )
                                } else {
                                    Text(
                                        text = "EnvÃ­o Gratis en compras sobre ${formatoMoneda.format(20000)}",
                                        style = MaterialTheme.typography.titleSmall,
                                        fontWeight = FontWeight.Bold
                                    )
                                }
                            }
                        }
                    }

                    Spacer(modifier = Modifier.height(16.dp))

                    // Lista de productos
                    LazyColumn(
                        modifier = Modifier.weight(1f),
                        verticalArrangement = Arrangement.spacedBy(12.dp)
                    ) {
                        items(productos) { producto ->
                            ProductoCarritoItem(
                                producto = producto,
                                formatoMoneda = formatoMoneda,
                                onCantidadChanged = { nuevaCantidad ->
                                    carritoViewModel.actualizarCantidad(producto, nuevaCantidad)
                                },
                                onEliminar = {
                                    carritoViewModel.eliminarProducto(producto)
                                }
                            )
                        }
                    }

                    Spacer(modifier = Modifier.height(16.dp))

                    // Resumen de costos
                    Card(
                        modifier = Modifier.fillMaxWidth(),
                        colors = CardDefaults.cardColors(
                            containerColor = MaterialTheme.colorScheme.surface.copy(alpha = 0.9f)
                        )
                    ) {
                        Column(
                            modifier = Modifier.padding(16.dp),
                            verticalArrangement = Arrangement.spacedBy(8.dp)
                        ) {
                            Row(
                                modifier = Modifier.fillMaxWidth(),
                                horizontalArrangement = Arrangement.SpaceBetween
                            ) {
                                Text("Subtotal:")
                                Text(formatoMoneda.format(subtotal))
                            }
                            Row(
                                modifier = Modifier.fillMaxWidth(),
                                horizontalArrangement = Arrangement.SpaceBetween
                            ) {
                                Text("IVA (19%):")
                                Text(formatoMoneda.format(iva))
                            }
                            Row(
                                modifier = Modifier.fillMaxWidth(),
                                horizontalArrangement = Arrangement.SpaceBetween
                            ) {
                                Text("EnvÃ­o:")
                                if (calificaEnvioGratis) {
                                    Text(
                                        text = "GRATIS",
                                        fontWeight = FontWeight.Bold,
                                        color = MaterialTheme.colorScheme.primary
                                    )
                                } else {
                                    Text(formatoMoneda.format(costoEnvio))
                                }
                            }
                            HorizontalDivider()
                            Row(
                                modifier = Modifier.fillMaxWidth(),
                                horizontalArrangement = Arrangement.SpaceBetween
                            ) {
                                Text(
                                    text = "Total:",
                                    style = MaterialTheme.typography.titleLarge,
                                    fontWeight = FontWeight.Bold
                                )
                                Text(
                                    text = formatoMoneda.format(total),
                                    style = MaterialTheme.typography.titleLarge,
                                    fontWeight = FontWeight.Bold,
                                    color = MaterialTheme.colorScheme.primary
                                )
                            }
                        }
                    }

                    Spacer(modifier = Modifier.height(16.dp))

                    // BotÃ³n proceder al pago
                    Button(
                        onClick = { navController.navigate("checkout") },
                        modifier = Modifier
                            .fillMaxWidth()
                            .height(56.dp),
                        enabled = productos.isNotEmpty()
                    ) {
                        Text(
                            text = "Proceder al Pago",
                            style = MaterialTheme.typography.titleMedium
                        )
                    }
                }
            }
        }
        }
    }
}

@Composable
private fun ProductoCarritoItem(
    producto: ProductoCarrito,
    formatoMoneda: NumberFormat,
    onCantidadChanged: (Int) -> Unit,
    onEliminar: () -> Unit
) {
    Card(
        modifier = Modifier.fillMaxWidth(),
        colors = CardDefaults.cardColors(
            containerColor = MaterialTheme.colorScheme.surface.copy(alpha = 0.9f)
        )
    ) {
        Row(
            modifier = Modifier
                .fillMaxWidth()
                .padding(12.dp),
            horizontalArrangement = Arrangement.spacedBy(12.dp)
        ) {
            Image(
                painter = painterResource(id = producto.imagenResId),
                contentDescription = "Imagen de ${producto.nombre}",
                modifier = Modifier
                    .size(80.dp)
                    .clip(RoundedCornerShape(8.dp))
                    .background(MaterialTheme.colorScheme.surfaceVariant),
                contentScale = ContentScale.Crop
            )

            // InformaciÃ³n del producto
            Column(
                modifier = Modifier.weight(1f),
                verticalArrangement = Arrangement.spacedBy(4.dp)
            ) {
                Text(
                    text = producto.nombre,
                    style = MaterialTheme.typography.titleMedium,
                    fontWeight = FontWeight.Bold
                )

                if (producto.talla != null) {
                    Text(
                        text = "Talla: ${producto.talla.displayName}",
                        style = MaterialTheme.typography.bodySmall,
                        color = MaterialTheme.colorScheme.onSurface.copy(alpha = 0.7f)
                    )
                }

                Text(
                    text = "Color: ${producto.color.nombre}",
                    style = MaterialTheme.typography.bodySmall,
                    color = MaterialTheme.colorScheme.onSurface.copy(alpha = 0.7f)
                )

                Spacer(modifier = Modifier.height(4.dp))

                Row(
                    verticalAlignment = Alignment.CenterVertically,
                    horizontalArrangement = Arrangement.SpaceBetween,
                    modifier = Modifier.fillMaxWidth()
                ) {
                    // Control de cantidad
                    Row(
                        verticalAlignment = Alignment.CenterVertically,
                        horizontalArrangement = Arrangement.spacedBy(8.dp)
                    ) {
                        IconButton(
                            onClick = { onCantidadChanged(producto.cantidad - 1) },
                            modifier = Modifier.size(32.dp)
                        ) {
                            Text(
                                text = "âˆ’",
                                style = MaterialTheme.typography.titleLarge
                            )
                        }

                        Text(
                            text = producto.cantidad.toString(),
                            style = MaterialTheme.typography.titleMedium,
                            modifier = Modifier.widthIn(min = 24.dp)
                        )

                        IconButton(
                            onClick = { onCantidadChanged(producto.cantidad + 1) },
                            modifier = Modifier.size(32.dp)
                        ) {
                            Icon(
                                imageVector = Icons.Default.Add,
                                contentDescription = "Aumentar cantidad",
                                modifier = Modifier.size(16.dp)
                            )
                        }
                    }

                    // Precio y botÃ³n eliminar
                    Column(
                        horizontalAlignment = Alignment.End
                    ) {
                        Text(
                            text = formatoMoneda.format(producto.subtotal()),
                            style = MaterialTheme.typography.titleMedium,
                            fontWeight = FontWeight.Bold,
                            color = MaterialTheme.colorScheme.primary
                        )
                        IconButton(
                            onClick = onEliminar,
                            modifier = Modifier.size(24.dp)
                        ) {
                            Icon(
                                imageVector = Icons.Default.Delete,
                                contentDescription = "Eliminar",
                                tint = MaterialTheme.colorScheme.error,
                                modifier = Modifier.size(20.dp)
                            )
                        }
                    }
                }
            }
        }
    }
}
